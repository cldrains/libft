#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;
	unsigned char	character;

	str = (unsigned char *)s;
	character = (unsigned char)c;
	i = -1;
	while (++i < n)
		if (str[i] == character)
			return (str + i);
	return (NULL);
}
